def test_industry_optimized():
    print("🚀 [Final Stage] 正在基于真实原始数据结构进行字段对齐...")
    
    try:
        # 1. 抓取数据
        df = ak.stock_sector_spot()
        
        if df is None or df.empty:
            print("❌ 接口返回为空。")
            return

        # 2. 精准映射 (基于你提供的原始数据列名)
        # 原始列名 -> 你的业务变量名
        column_map = {
            '板块': '行业名称',
            '总成交额': '成交额_元',  # 原始列名里有“总”字
            '涨跌幅': '涨跌幅',        # 虽然一样，但也写上明确意图
            '股票名称': '领涨股票'      # 原始数据里这个字段其实是该板块的龙头股
        }
        
        # 只重命名确实存在的列，防止报错
        existing_map = {k: v for k, v in column_map.items() if k in df.columns}
        df.rename(columns=existing_map, inplace=True)

        # 3. 数据清洗与转换
        # 转换数字类型
        df['涨跌幅'] = pd.to_numeric(df['涨跌幅'], errors='coerce').fillna(0)
        
        # 处理成交额：元 -> 亿
        if '成交额_元' in df.columns:
            df['成交额_元'] = pd.to_numeric(df['成交额_元'], errors='coerce').fillna(0)
            df['成交额'] = round(df['成交额_元'] / 100000000, 2)
        else:
            # 万一改名失败的保底逻辑
            df['成交额'] = 0

        # 4. 结果验证预览
        print("\n--- ✅ 字段对齐成功，结果预览 ---")
        # 按照涨跌幅排序看前 5 名
        top_5 = df.nlargest(5, '涨跌幅')
        
        # 打印列名确认结果
        print(f"当前 Dataframe 列名: {df.columns.tolist()}\n")
        
        for i, row in top_5.iterrows():
            # 这里的字段名现在必须完全匹配你 rename 之后的名字
            print(f"行业: {row['行业名称']:<10} | 涨幅: {row['涨跌幅']:>6.2f}% | 领涨股: {row['领涨股票']:<8} | 成交: {row['成交额']}亿")

        return df

    except Exception as e:
        print(f"❌ 优化测试崩溃: {e}")
        # 打印堆栈信息便于定位具体是哪一行 KeyError
        import traceback
        traceback.print_exc()

